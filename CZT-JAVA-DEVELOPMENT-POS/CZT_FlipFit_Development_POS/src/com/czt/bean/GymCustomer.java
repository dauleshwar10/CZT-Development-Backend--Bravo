package com.czt.bean;

/**
 * 
 */
public class GymCustomer {
	private int custId;
	private String custName;
	private String custEmail;
	private int custAge;
	private int custMobile;

	/**
	 * @return the custId
	 */
	public int getCustId() {
		return custId;
	}

	/**
	 * @param custId the custId to set
	 */
	public void setCustId(int custId) {
		this.custId = custId;
	}

	/**
	 * @return the custName
	 */
	public String getCustName() {
		return custName;
	}

	/**
	 * @param custName the custName to set
	 */
	public void setCustName(String custName) {
		this.custName = custName;
	}

	/**
	 * @return the custEmail
	 */
	public String getCustEmail() {
		return custEmail;
	}

	/**
	 * @param custEmail the custEmail to set
	 */
	public void setCustEmail(String custEmail) {
		this.custEmail = custEmail;
	}

	/**
	 * @return the custAge
	 */
	public int getCustAge() {
		return custAge;
	}

	/**
	 * @param custAge the custAge to set
	 */
	public void setCustAge(int custAge) {
		this.custAge = custAge;
	}

	/**
	 * @return the custMobile
	 */
	public int getCustMobile() {
		return custMobile;
	}

	/**
	 * @param custMobile the custMobile to set
	 */
	public void setCustMobile(int custMobile) {
		this.custMobile = custMobile;
	}

}