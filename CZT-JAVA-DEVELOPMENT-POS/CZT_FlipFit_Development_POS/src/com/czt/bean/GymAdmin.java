package com.czt.bean;

import java.util.Date;

/**
 * 
 */
public class GymAdmin {
	private int adminId;
	private String adminName;
	private int adminmobileNumber;
	private String adminEmailId;
	private Date adminDOB;

	/**
	 * @return the adminId
	 */
	public int getAdminId() {
		return adminId;
	}

	/**
	 * @param adminId the adminId to set
	 */
	public void setAdminId(int adminId) {
		this.adminId = adminId;
	}

	/**
	 * @return the adminName
	 */
	public String getAdminName() {
		return adminName;
	}

	/**
	 * @param adminName the adminName to set
	 */
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}

	/**
	 * @return the adminmobileNumber
	 */
	public int getAdminmobileNumber() {
		return adminmobileNumber;
	}

	/**
	 * @param adminmobileNumber the adminmobileNumber to set
	 */
	public void setAdminmobileNumber(int adminmobileNumber) {
		this.adminmobileNumber = adminmobileNumber;
	}

	/**
	 * @return the adminEmailId
	 */
	public String getAdminEmailId() {
		return adminEmailId;
	}

	/**
	 * @param adminEmailId the adminEmailId to set
	 */
	public void setAdminEmailId(String adminEmailId) {
		this.adminEmailId = adminEmailId;
	}

	/**
	 * @return the adminDOB
	 */
	public Date getAdminDOB() {
		return adminDOB;
	}

	/**
	 * @param adminDOB the adminDOB to set
	 */
	public void setAdminDOB(Date adminDOB) {
		this.adminDOB = adminDOB;
	}

}
