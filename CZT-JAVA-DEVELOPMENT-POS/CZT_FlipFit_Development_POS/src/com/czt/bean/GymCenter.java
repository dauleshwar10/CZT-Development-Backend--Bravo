package com.czt.bean;

/**
 * 
 */
public class GymCenter {
	private int gymId;
	private String gymName;
	private String gymaddress;
	private int gymCapacity;
	private String gymLocation;
	private int pinCode;

	/**
	 * @return the gymId
	 */
	public int getGymId() {
		return gymId;
	}

	/**
	 * @param gymId the gymId to set
	 */
	public void setGymId(int gymId) {
		this.gymId = gymId;
	}

	/**
	 * @return the gymName
	 */
	public String getGymName() {
		return gymName;
	}

	/**
	 * @param gymName the gymName to set
	 */
	public void setGymName(String gymName) {
		this.gymName = gymName;
	}

	/**
	 * @return the gymaddress
	 */
	public String getGymaddress() {
		return gymaddress;
	}

	/**
	 * @param gymaddress the gymaddress to set
	 */
	public void setGymaddress(String gymaddress) {
		this.gymaddress = gymaddress;
	}

	/**
	 * @return the gymCapacity
	 */
	public int getGymCapacity() {
		return gymCapacity;
	}

	/**
	 * @param gymCapacity the gymCapacity to set
	 */
	public void setGymCapacity(int gymCapacity) {
		this.gymCapacity = gymCapacity;
	}

	/**
	 * @return the gymLocation
	 */
	public String getGymLocation() {
		return gymLocation;
	}

	/**
	 * @param gymLocation the gymLocation to set
	 */
	public void setGymLocation(String gymLocation) {
		this.gymLocation = gymLocation;
	}

	/**
	 * @return the pinCode
	 */
	public int getPinCode() {
		return pinCode;
	}

	/**
	 * @param pinCode the pinCode to set
	 */
	public void setPinCode(int pinCode) {
		this.pinCode = pinCode;
	}

}
